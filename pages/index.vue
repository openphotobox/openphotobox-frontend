// Fetch real assets from API with pagination
const assets = ref([])
const pending = ref(true)
const loadingMore = ref(false)
const error = ref(null)
const hasNextPage = ref(true)
const nextCursor = ref(null)
const totalCount = ref(0)
const showAssetDialog = ref(false)
const selectedAsset = ref(null)
const sidebarCollapsed = ref(true)
const selectedYear = ref(null)

// JustifiedGallery component handles the layout algorithm

// Fetch initial assets
const fetchAssets = async (reset = true) => {
  try {
    if (reset) {
      pending.value = true
      assets.value = []
      nextCursor.value = null
      hasNextPage.value = true
    } else {
      loadingMore.value = true
    }
    error.value = null
    
    console.log('Fetching assets...', reset ? '(initial)' : '(load more)')
    
    const params = { limit: 50 } // Smaller page size for better performance
    if (nextCursor.value && !reset) {
      // Extract the cursor value from the URL if it's a full URL
      let cursorValue = nextCursor.value
      if (cursorValue.includes('cursor=')) {
        const url = new URL(cursorValue)
        cursorValue = url.searchParams.get('cursor') || cursorValue
      }
      params.cursor = cursorValue
      console.log('Using cursor for pagination:', nextCursor.value)
      console.log('Extracted cursor value:', cursorValue)
    }
    
    const response = await api.assets.list(params)
    console.log('API response:', response)
    console.log('API response data:', response.data)
    console.log('Pagination info:', {
      count: response.data?.count,
      next: response.data?.next,
      previous: response.data?.previous,
      resultsLength: response.data?.results?.length
    })
    
    if (response.success) {
      // The response from DRF with pagination will have a 'results' array
      const assetsList = response.data?.results || response.data || []
      
      if (Array.isArray(assetsList)) {
        console.log('Found assets array with length:', assetsList.length)
        const mappedAssets = assetsList.map(asset => ({
          ...asset,
          // Use the thumbnail and original URLs from the API
          // These are now properly generated by the backend serializer
          faces: asset.faces || []
        }))
        
        if (reset) {
          assets.value = mappedAssets
        } else {
          assets.value = [...assets.value, ...mappedAssets]
        }
        
        console.log('Updated assets array length:', assets.value.length)
        
        // Update pagination state
        hasNextPage.value = !!response.data?.next
        nextCursor.value = response.data?.next || null
        
        console.log('Pagination state:', {
          hasNextPage: hasNextPage.value,
          nextCursor: nextCursor.value,
          totalCount: response.data?.count
        })
        
        // Update total count (only on first load)
        if (reset && response.data?.count !== undefined) {
          totalCount.value = response.data.count
        }
        
        // Debug: Check thumbnail URLs
        if (assets.value.length > 0) {
          console.log('First asset thumbnail_url:', assets.value[0].thumbnail_url)
          console.log('First asset original_url:', assets.value[0].original_url)
          console.log('First asset storage_url:', assets.value[0].storage_url)
        }
      } else {
        console.log('No assets in response or invalid format')
        if (reset) {
          assets.value = []
        }
      }
    } else {
      console.error('API error:', response.error)
      const errorMessage = response.error || 'Failed to fetch assets'
      
      // Handle cursor pagination errors specifically
      if (errorMessage.includes('Invalid cursor') || errorMessage.includes('cursor')) {
        console.log('Cursor pagination error, resetting pagination state')
        // Reset pagination state and try again without cursor
        nextCursor.value = null
        hasNextPage.value = false
        if (!reset) {
          // If this was a "load more" request, just stop trying to load more
          console.log('Stopping pagination due to cursor error')
          return
        }
      }
      
      error.value = new Error(errorMessage)
      if (reset) {
        assets.value = []
      }
    }
  } catch (err) {
    console.error('Error fetching assets:', err)
    error.value = err
    if (reset) {
      assets.value = []
    }
  } finally {
    pending.value = false
    loadingMore.value = false
  }
}

// Load more assets (manual button click)
const loadMoreAssets = async () => {
  if (loadingMore.value || !hasNextPage.value) {
    return
  }
  
  try {
    await fetchAssets(false)
    // Let the masonry handle the layout naturally with overflow-anchor disabled
  } catch (err) {
    console.error('Failed to load more assets:', err)
    // Don't set error state for pagination failures, just log them
    // The user can still see the existing content
  }
}

// Force load more assets (debug function)
const forceLoadMore = async () => {
  console.log('Force loading more assets...')
  console.log('Current state:', {
    hasNextPage: hasNextPage.value,
    nextCursor: nextCursor.value,
    assetsLength: assets.value.length,
    totalCount: totalCount.value
  })
  
  // Reset pagination state and try again
  hasNextPage.value = true
  nextCursor.value = null
  
  try {
    await fetchAssets(false)
  } catch (err) {
    console.error('Force load failed:', err)
  }
}

