// Infinite scroll setup using Intersection Observer
const setupInfiniteScroll = () => {
  // Wait a bit for the DOM to be ready
  setTimeout(() => {
    const photoGridContent = document.querySelector('.photo-grid-content')
    if (!photoGridContent) {
      console.log('Photo grid content not found, retrying...')
      setTimeout(setupInfiniteScroll, 1000)
      return
    }
    
    // Remove any existing sentinel and observer
    const existingSentinel = document.getElementById('infinite-scroll-sentinel')
    if (existingSentinel) {
      existingSentinel.remove()
    }
    
    if (window.infiniteScrollObserver) {
      window.infiniteScrollObserver.disconnect()
    }
    
    // Create a sentinel element at the bottom of the content
    const sentinel = document.createElement('div')
    sentinel.id = 'infinite-scroll-sentinel'
    sentinel.style.height = '20px'
    sentinel.style.width = '100%'
    sentinel.style.background = 'transparent'
    sentinel.style.pointerEvents = 'none' // Prevent any interaction
    
    // Add sentinel to the end of the content
    photoGridContent.appendChild(sentinel)
    
    console.log('Setting up infinite scroll with sentinel')
    
    // Create intersection observer with debouncing
    let isLoading = false
    const observer = new IntersectionObserver(
      (entries) => {
        const entry = entries[0]
        console.log('Sentinel intersection:', entry.isIntersecting, 'hasNextPage:', hasNextPage.value, 'loadingMore:', loadingMore.value, 'isLoading:', isLoading)
        
        if (entry.isIntersecting && hasNextPage.value && !loadingMore.value && !isLoading) {
          isLoading = true
          console.log('Loading more assets...')
          
          // Temporarily disconnect observer to prevent multiple triggers
          observer.disconnect()
          
          loadMoreAssets().finally(() => {
            // Reconnect observer after loading is complete
            setTimeout(() => {
              observer.observe(sentinel)
              isLoading = false
            }, 500)
          })
        }
      },
      {
        root: photoGridContent,
        rootMargin: '100px', // Reduced from 200px to be less aggressive
        threshold: 0.1
      }
    )
    
    observer.observe(sentinel)
    
    // Store observer for cleanup
    window.infiniteScrollObserver = observer
  }, 500)
}

// Fetch assets when component mounts
onMounted(async () => {
  await fetchTimeline()
  sectionObserver.current = new IntersectionObserver((entries) => {
    for (const entry of entries) {
      if (entry.isIntersecting) {
        const date = elToDate.get(entry.target)
        if (date) {
          currentDate.value = date
          loadDateAssets(date)
        }
      }
    }
  }, { root: null, rootMargin: '200px', threshold: 0.1 })

  // Preload the first couple of dates so content appears immediately
  if (timelineItems.value && timelineItems.value.length) {
    const preload = timelineItems.value.slice(0, 2)
    for (const item of preload) {
      loadDateAssets(item.date)
    }
  }

  // SSE: refresh the currently visible date section on asset_ready
  if ($events?.onAssetReady) {
    $events.onAssetReady(async () => {
      try {
        // Reload current date bucket and maybe the previous one for smoothness
        const cur = currentDate.value
        if (cur) {
          // Clear and reload the date assets
          delete dateAssets[cur]
          await loadDateAssets(cur)
        }
      } catch {}
    })
  }

  // Initialize scroll tracking after mount
  // Track active date smoothly on scroll (bind after DOM paints)
  setTimeout(() => {
    updateActiveOnScroll()
    if (contentRef.value && contentRef.value.addEventListener) {
      contentRef.value.addEventListener('scroll', updateActiveOnScroll, { passive: true })
    } else {
      window.addEventListener('scroll', updateActiveOnScroll, { passive: true })
    }
  }, 0)
})

onUnmounted(() => {
  if (sectionObserver.current) {
    sectionObserver.current.disconnect()
    sectionObserver.current = null
  }
  elToDate.clear()
  // No window resize handler required
  if (contentRef.value && contentRef.value.removeEventListener) {
    contentRef.value.removeEventListener('scroll', updateActiveOnScroll)
  } else {
    window.removeEventListener('scroll', updateActiveOnScroll)
  }
})

const openAsset = (asset) => {
  selectedAsset.value = asset
  showAssetDialog.value = true
}

const formatDate = (dateString) => {
  if (!dateString) return 'Unknown date'
  return new Date(dateString).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  })
}

const formatDateHeader = (dateString) => {
  const date = new Date(dateString)
  const today = new Date()
  const yesterday = new Date(today)
  yesterday.setDate(yesterday.getDate() - 1)
  
  if (date.toDateString() === today.toDateString()) {
    return 'Today'
  } else if (date.toDateString() === yesterday.toDateString()) {
    return 'Yesterday'
  } else {
    return date.toLocaleDateString('en-US', {
      weekday: 'long',
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    })
  }
}

// Group assets by date
const groupedAssets = computed(() => {
  const groups = {}
  
  assets.value.forEach(asset => {
    const date = new Date(asset.taken_at || asset.created_at)
    const dateKey = date.toISOString().split('T')[0] // YYYY-MM-DD format
    
    if (!groups[dateKey]) {
      groups[dateKey] = {
        date: dateKey,
        assets: []
      }
    }
    groups[dateKey].assets.push(asset)
  })
  
  // Sort by date (newest first)
  return Object.values(groups).sort((a, b) => new Date(b.date) - new Date(a.date))
})

// Generate timeline years from assets with months
const timelineYears = computed(() => {
  const yearData = {}
  
  assets.value.forEach(asset => {
    const date = new Date(asset.taken_at || asset.created_at)
    const year = date.getFullYear()
    const month = date.getMonth()
    
    if (!yearData[year]) {
      yearData[year] = {
        year: year,
        count: 0,
        months: {}
      }
    }
    
    yearData[year].count++
    
    if (!yearData[year].months[month]) {
      yearData[year].months[month] = 0
    }
    yearData[year].months[month]++
  })
  
  return Object.values(yearData)
    .sort((a, b) => b.year - a.year)
    .map(yearData => ({
      ...yearData,
      months: Object.entries(yearData.months)
        .map(([month, count]) => ({
          month: parseInt(month),
          count,
          name: new Date(0, month).toLocaleString('default', { month: 'short' })
        }))
        .sort((a, b) => b.month - a.month)
    }))
})

